let currentLang = 'ms';
let memberType = 'individual';

function setLang(lang) {
  currentLang = lang;
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
  document.querySelectorAll('.lang-btn').forEach(b => {
    if (b.textContent.trim() === (lang==='ms'?'BM':lang==='en'?'EN':'中文')) b.classList.add('active');
  });
  document.documentElement.lang = lang === 'ms' ? 'ms' : lang === 'en' ? 'en' : 'zh';
  applyTranslations();
}

function applyTranslations() {
  const t = T[currentLang];
  // Text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  // Placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key].replace(/&amp;/g,'&');
  });
  // Update fee note based on current member type
  updateFeeNote();
}

function updateFeeNote() {
  const note = document.getElementById('feeNote');
  if (!note) return;                       // register form not present (home / news pages)
  const t = T[currentLang];
  if (memberType === 'corporate') {
    note.innerHTML = '<strong>' + t.fee_corp_label + '</strong> ' + t.fee_corp_text;
  } else if (memberType === 'student') {
    note.innerHTML = '<strong>' + t.fee_stu_label + '</strong> ' + t.fee_stu_text;
    note.style.background = '#e6f9ee';
    note.style.borderColor = '#27ae60';
    note.style.color = '#1a7a40';
  } else {
    note.innerHTML = '<strong>' + t.fee_label + '</strong> ' + t.fee_ind_text;
    note.style.background = '';
    note.style.borderColor = '';
    note.style.color = '';
  }
}

function setType(type, el) {
  memberType = type;
  document.querySelectorAll('.toggle-btn').forEach(b => b.classList.remove('active'));
  el.classList.add('active');
  const icInput = document.getElementById('icnum');
  const stuGroup = document.getElementById('studentGroup');
  const emailHint = document.getElementById('emailHint');

  // reset all
  document.getElementById('orgGroup').style.display = 'none';
  document.getElementById('icGroup').style.display = 'none';
  stuGroup.style.display = 'none';
  emailHint.style.display = 'none';
  icInput.required = false;
  icInput.disabled = true;

  // set required on student fields to false before toggling
  ['stuInstitution','stuCourse','stuMatric','stuIdFile','lecName','lecEmail'].forEach(id => {
    const el2 = document.getElementById(id);
    if (el2) { el2.required = false; el2.disabled = true; }
  });

  if (type === 'corporate') {
    document.getElementById('orgGroup').style.display = '';
  } else if (type === 'student') {
    stuGroup.style.display = '';
    emailHint.style.display = 'block';
    ['stuInstitution','stuCourse','stuMatric','stuIdFile','lecName','lecEmail'].forEach(id => {
      const el2 = document.getElementById(id);
      if (el2) { el2.required = (id !== 'lecEmail'); el2.disabled = false; }
    });
    // lecEmail not strictly required but encouraged
    document.getElementById('lecEmail').required = true;
    document.getElementById('lecEmail').disabled = false;
  } else {
    document.getElementById('icGroup').style.display = '';
    icInput.required = true;
    icInput.disabled = false;
  }
  updateFeeNote();
}

